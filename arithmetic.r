# Arithmetic operators

a <- 5
b <- 10
c <- 2
d <- 0
e <- 3

add = a + b
sub = b - c
mul = a * c
div = b / e
intdiv = b %/% e
mod = e %% c
ex = e^d

message("sum of ",a," and ",b," = ",add)
message("Difference of ",b," and ",c," = ",sub)
message("Product of ",a," and ",b," = ",mul)
message("Division ",b," and ",e," = ",div)
message("Integer Division ",b," and ",e," = ",intdiv)
message("modulus of ",e," and ",c," = ",mod)
message("exponent of ",e," and ",d," = ",ex)
